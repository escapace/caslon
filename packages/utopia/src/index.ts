export const typeScaleToString = (config: { prefix?: string } & UtopiaTypeConfig): string => {
  const scales = calculateTypeScale(config)
  const prefix = config.prefix ?? 'type'

  const wcagViolations = scales
    .map((value) => value.wcagViolation)
    .filter((value) => value !== undefined)

  if (wcagViolations.length !== 0) {
    throw new Error(
      `WCAG 1.4.4 violation(s): ${wcagViolations.map((value) => `${value.from}-${value.to}`).join(', ')}`,
    )
  }

  return [
    ':root {',
    ...scales.flatMap((value) => [
      `  /* ${value.label}: ${value.minFontSize}px → ${value.maxFontSize}px */`,
      `  --${prefix}-${value.label}: ${value.clamp};`,
    ]),
    '}',
  ].join('\n')
}

